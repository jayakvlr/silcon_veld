"""
This module provides a simple function for addition.
"""


def add(num1, num2):
    """
    Add two numbers.

    This function takes two numbers as input and returns their sum.

    :param a: The first number.
    :param b: The second number.
    :return: The sum of `a` and `b`."""
    return num1 + num2


print(add(1, 1))
